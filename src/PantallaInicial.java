    private void actualizarDatosTabla() {
        modelTablaContactos = new DefaultTableModel();
        modelTablaContactos.addColumn("ID");
        modelTablaContactos.addColumn("Nombre");
        modelTablaContactos.addColumn("Apellidos");
        modelTablaContactos.addColumn("Dirección");
        
        List<Contacto> contactos = ContactoService.getAll();
        
        for(Contacto contacto: contactos) {
            Object[] fila = { contacto.getIdContacto(), contacto.getNombres(), contacto.getApellidos(), contacto.getDireccion()  };
            modelTablaContactos.addRow(fila);
        }
        
        tblContactos.setModel(modelTablaContactos);        
    }
    
    private void limpiarCampos() {
        tfID.setText("");
        tfNombre.setText("");
        tfApellido.setText("");
        tfDireccion.setText("");
    }
    
    private void eliminarRegistro() {
        if(tblContactos.getSelectedRow() != -1)
        {
            String idContactoString = tblContactos.getValueAt(tblContactos.getSelectedRow(), 0).toString();
            int idContacto = Integer.parseInt( idContactoString );
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Desea eliminar el contacto seleccionado?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if(confirmacion == JOptionPane.YES_OPTION) {
                ContactoService.delete(idContacto);

                actualizarDatosTabla();
                limpiarCampos();
                JOptionPane.showMessageDialog(this, "Registro eliminado con éxito");
            }
        }
    }

