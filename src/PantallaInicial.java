    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String idContacto = tfID.getText().trim();
        
        if(idContacto.isEmpty()){
            //Guardado
            String nombres = tfNombre.getText();
            String apellidos = tfApellido.getText();
            String direccion = tfDireccion.getText();

            Contacto nuevoContacto = new Contacto(nombres, apellidos, direccion);
            ContactoService.create(nuevoContacto);

            limpiarCampos();

            actualizarDatosTabla();

            JOptionPane.showMessageDialog(this, "Contacto creado correctamente");
        
        }else{
            
            int idContactoToEdit = Integer.parseInt(idContacto);
            String nombres = tfNombre.getText();
            String apellidos = tfApellido.getText();
            String direccion = tfDireccion.getText();
            
            Contacto editContacto = new Contacto(idContactoToEdit, nombres, apellidos, direccion);
            ContactoService.edit(editContacto);
            
            limpiarCampos();
            
            actualizarDatosTabla();
            
            JOptionPane.showMessageDialog(this, "Contacto editado correctamente");
            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

